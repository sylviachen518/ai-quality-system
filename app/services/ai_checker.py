def build_article_text(raw_text: str) -> str:
    lines = raw_text.strip().split("\n")

    title = ""
    content_lines = []

    # 取第一個非空行當標題
    for i, line in enumerate(lines):
        if line.strip():
            title = line.strip()
            content_lines = lines[i+1:]
            break

    content = "\n".join(content_lines).strip()

    paragraphs = [p.strip() for p in content.split("\n\n") if p.strip()]

    article_text = ""

    if title:
        article_text += f"【標題】\n{title}\n\n"

    if paragraphs:
        article_text += "【正文】\n"
        for idx, para in enumerate(paragraphs):
            article_text += f"第{idx+1}段：{para}\n\n"
    else:
        article_text += "【正文】\n（無正文內容，僅有標題）"

    return article_text


def check_text(raw_text: str):

    article_text = build_article_text(raw_text)

    response = client.chat.completions.create(
        model="gpt-4o-mini",
        temperature=0,
        messages=[
            {"role": "system", "content": SYSTEM_PROMPT},
            {
                "role": "user",
                "content": f"請嚴格按照規則逐字逐詞檢查以下文章：\n\n{article_text}"
            }
        ]
    )

    return response.choices[0].message.content